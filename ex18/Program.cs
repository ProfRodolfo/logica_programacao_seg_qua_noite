// Ex18: Escreva um programa que utilize um laço `for` para imprimir todos os números inteiros de 1 a 10, um em cada linha.

// For
for (int a = 1; a <=10 ; a++)
{
    Console.WriteLine(a);
}

int i  = 1;

// Laço while
while (i <= 10)
{
    Console.WriteLine(i);
    i++;

}
// Laço Do while
do
{
    Console.WriteLine(i);
    i++;
} while (i <= 10);